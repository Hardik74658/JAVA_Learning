public class Swap{

	public static void main(String args[]){
		int n1,n2,temp;
		n1 = 10;
		n2 = 5;
		System.out.println("Before Swaping  ");
		System.out.println("No.1 : "+n1);
		System.out.println("No.2 : "+n2);
		temp = n1;
		n1 = n2;
		n2 = temp;
		System.out.println("After Swaping  ");
		System.out.println("No.1 : "+n1);
		System.out.println("No.2 : "+n2);

	}

}